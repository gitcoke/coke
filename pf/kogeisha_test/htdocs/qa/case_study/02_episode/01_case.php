			<h2 class="wide">万一の場合の必要保障額は、どうやって計算すればいいの？</h2>
			<p class="head646x153S" id="case1_talk1"><span>万一の必要保障額は、どうやって計算すればいいの？？</span></p>
			<p class="head646x186" id="case1_talk2"><span>必要保障額は「残された遺族が、その後、ある程度の生活レベルを維持していくのに必要な金額」と考えれば分かりやすいと思います。</span></p>
			<p>遺族にも遺族年金やパート収入など、収入が見込めるものもあるので、それらでカバーできない部分の金額の総計と考えればいいのです。</p>
			<div class="margin-bottom12px margin-top18px margin-left15px"><img src="img/case1_fig1.gif" width="616" height="145" alt="必要保障額 = (A)遺族がこれからつかうお金の合計 + (B)遺族の収入" /></div>
			<p class="text-bold margin-top24px">(A)遺族がこれから使うお金は</p>
			<div class="listCheck">
				<ul>
					<li>まず、住むところの確保が重要です</li>
					<li>ご主人の給料が止まるので日々の生活費</li>
					<li>赤ちゃんの学費</li>
					<li>お葬式やお墓の費用</li>
				</ul>
			</div>
			<p class="text-bold margin-top24px">(B)の遺族の収入は</p>
			<div class="listCheck">
				<ul>
					<li>国の年金制度からの、遺族年金</li>
					<li>勤務先・企業からの、ご主人の死亡退職金・弔慰金</li>
					<li>もちろん、配偶者の方が就職されればその収入も加算されます。</li>
					<li>1〜3で不足する分を、ご主人の“生命保険”による収入でカバーするのです。</li>
				</ul>
			</div>
			
			<p><b><span class="text-red">遺族が必要とする（これから使う）資金の総額から、遺族が受け取れる収入(遺族年金や死亡退職金など)を差し引いたものが、民間の生命保険でカバーすべき“必要保障額”となります。</span></b></p>
			
			<p>必要保障額をキチンと見直したら、保険金額を減らすことができるかもしれませんね！</p>
